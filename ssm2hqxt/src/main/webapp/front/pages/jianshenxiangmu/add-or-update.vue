		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取
			this.ruleForm.jianshenfangmingcheng = this.user.jianshenfangmingcheng
			this.ro.jianshenfangmingcheng = true;
			this.ruleForm.jianshenfangdizhi = this.user.jianshenfangdizhi
			this.ro.jianshenfangdizhi = true;


