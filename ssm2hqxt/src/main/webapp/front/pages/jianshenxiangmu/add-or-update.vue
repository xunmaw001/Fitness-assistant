			this.jianshenxiangmuOptions = res.data;

			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`jianshenxiangmu`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='xiangmubianhao'){
					this.ruleForm.xiangmubianhao = obj[o];
					this.ro.xiangmubianhao = true;
					continue;
					}
					if(o=='jianshenxiangmu'){
					this.ruleForm.jianshenxiangmu = obj[o];
					this.ro.jianshenxiangmu = true;
					continue;
					}
					if(o=='xiangmufengmian'){
					this.ruleForm.xiangmufengmian = obj[o];
					this.ro.xiangmufengmian = true;
					continue;
					}
					if(o=='jianshenleixing'){
					this.ruleForm.jianshenleixing = obj[o];
					this.ro.jianshenleixing = true;
					continue;
					}
					if(o=='shiherenqun'){
					this.ruleForm.shiherenqun = obj[o];
					this.ro.shiherenqun = true;
					continue;
					}
					if(o=='xiangmujiage'){
					this.ruleForm.xiangmujiage = obj[o];
					this.ro.xiangmujiage = true;
					continue;
					}
					if(o=='keshishu'){
					this.ruleForm.keshishu = obj[o];
					this.ro.keshishu = true;
